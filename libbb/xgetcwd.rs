/* Return the current directory, newly allocated, arbitrarily long.
   Return NULL and set errno on error.
   If argument is not NULL (previous usage allocate memory), call free()
*/
#[no_mangle]
pub unsafe extern "C" fn xrealloc_getcwd_or_warn(mut cwd: *mut libc::c_char) -> *mut libc::c_char {
  let mut ret: *mut libc::c_char = 0 as *mut libc::c_char; /* 128 + 64 should be enough for 99% of cases */
  let mut path_max: libc::c_uint = 0;
  path_max = 128i32 as libc::c_uint;
  loop {
    path_max = path_max.wrapping_add(64i32 as libc::c_uint);
    cwd = xrealloc(cwd as *mut libc::c_void, path_max as size_t) as *mut libc::c_char;
    ret = getcwd(cwd, path_max as size_t);
    if ret.is_null() {
      if *bb_errno == 34i32 {
        continue;
      }
      free(cwd as *mut libc::c_void);
      bb_simple_perror_msg(b"getcwd\x00" as *const u8 as *const libc::c_char);
      return 0 as *mut libc::c_char;
    } else {
      cwd = xrealloc(
        cwd as *mut libc::c_void,
        strlen(cwd).wrapping_add(1i32 as libc::c_ulong),
      ) as *mut libc::c_char;
      return cwd;
    }
  }
}
